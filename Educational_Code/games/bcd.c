static void printcard( char * );
#define	bit(w,i)	((w)&(1<<(i)))
int main( int argc, char **argv ) {
	char cardline[80];
	if ( argc > 1 ) {
		while ( --argc )
			printcard( *++argv );
	} else while ( fgets( cardline, sizeof( cardline ), stdin ) )
		printcard( cardline );
	exit( 0 );
}
#define	COLUMNS	48
static void printcard( char *str ) {
	static char rowchars[] = "   123456789";
	int i, row;
	char *p;
	if ( ( p = strchr( str, '\n' ) ) ) *p = '\0';
	if ( strlen( str ) > COLUMNS ) str[COLUMNS] = '\0';
	for ( p = str; *p; ++p )
		if ( isascii( *p ) && islower( *p ) ) *p = toupper( *p );
	putchar( ' ' );
	for ( i = 1; i <= COLUMNS; ++i )
		putchar( '_' );
	putchar( '\n' );
	p = str;
	putchar( '/' );
	for ( i = 1; *p; i++, p++ )
		if ( holes[(unsigned char) *p] ) putchar( *p );
		else putchar( ' ' );
	while ( i++ <= COLUMNS )
		putchar( ' ' );
	putchar( '|' );
	putchar( '\n' );
	for ( row = 0; row <= 11; ++row ) {
		putchar( '|' );
		for ( i = 0, p = str; *p; i++, p++ ) {
			if ( bit( holes[(unsigned char )*p], 11 - row ) ) putchar( ']' );
			else putchar( rowchars[row] );
		}
		while ( i++ < COLUMNS )
			putchar( rowchars[row] );
		putchar( '|' );
		putchar( '\n' );
	}
	putchar( '|' );
	for ( i = 1; i <= COLUMNS; i++ )
		putchar( '_' );
	putchar( '|' );
	putchar( '\n' );
}
